package FatihObjectOrientedProgramming.Utils;

public class CalculatorMultipleUtil {
    public static int result;

    public int timeTable(int number, int table, int i) {
        if (table == table) {
            while (i <= i) {
                result = number * i;
                System.out.println(number + " * " + i + " = " + result);
                break;
            }
        }
        return result;
    }
}
