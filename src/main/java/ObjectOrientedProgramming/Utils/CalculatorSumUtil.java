package ObjectOrientedProgramming.Utils;

public class CalculatorSumUtil {
    public static int result;

    public int sumTable(int number, int i) {
        while (i <= i) {
            result = number + i;
            System.out.println(number + " + " + i + " = " + result);
            break;
        }
        return result;
    }
}
